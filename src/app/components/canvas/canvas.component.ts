  constructor(private renderer: Renderer2) {
    this.loadImages();
  }

  ngAfterViewInit(): void {
    this.context = (<HTMLCanvasElement>this.myCanvas.nativeElement).getContext('2d');
    this.animate();
  }

  ngOnInit() {
    this.listenForKeyboardInput();
  }

  private animate() {
    this.context.clearRect(0, 0, this.canvasWidth, this.canvasHeight);

    this.renderTilemap();
    this.renderViewWindow();

    // Draw the hero image
    // this.context.drawImage(this.hero, this.heroX, this.heroY);

      requestAnimationFrame(this.animate.bind(this));
  }

  private renderViewWindow(): void {
    this.context.beginPath();
    this.context.lineWidth = 6;
    this.context.strokeStyle = 'black';
    this.context.rect(this.viewX, this.viewY, this.viewWidth, this.viewHeight);
    this.context.stroke();
  }

  private renderTilemap(): void {
    for (let x = 0; x < this.tilesWide; x++) {
      for (let y = 0; y < this.tilesHigh; y++) {
        const tilePositionX = x * this.tileWidth;
        const tilePositionY = y * this.tileHeight;

        if (this.viewInsideWindow(tilePositionX, tilePositionY)) {
          this.context.drawImage(this.getTile(x, y), tilePositionX + this.tileOffsetX, tilePositionY + this.tileOffsetY, 50, 50);
        }
      }
    }
  }

  private viewInsideWindow(tilePositionX: number, tilePositionY: number): boolean {
    return tilePositionX + this.tileOffsetX + this.pixelsToRenderOutsideView > this.viewX &&
      this.viewX + this.viewWidth + this.pixelsToRenderOutsideView > tilePositionX + this.tileOffsetX &&
      tilePositionY + this.tileOffsetY + this.pixelsToRenderOutsideView > this.viewY &&
      this.viewY + this.viewHeight + this.pixelsToRenderOutsideView > tilePositionY + this.tileOffsetY;
  }

  private listenForKeyboardInput() {
    this.globalListenFunc = this.renderer.listen('document', 'keydown', e => {
      switch ((e.key as string).toLocaleLowerCase()) {
        case 'd':
          this.tileOffsetX -= 6;
          console.log(e.key + ' pressed');
          break;
        case 'a':
          this.tileOffsetX += 5;
          console.log(e.key + ' pressed');
          break;
        case 's':
          this.tileOffsetY -= 6;
          console.log(e.key + ' pressed');
          break;
        case 'w':
          this.tileOffsetY += 6;
          console.log(e.key + ' pressed');
          break;
      }
    });
  }

